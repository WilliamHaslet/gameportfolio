function OnLinkGroupHover(element)
{

    element.lastElementChild.style.borderBottomWidth = "4px";

}

function OnLinkGroupHoverThin(element)
{

    element.lastElementChild.style.borderBottomWidth = "3px";

}

function OnLinkGroupHoverEnd(element)
{

    element.lastElementChild.style.borderBottomWidth = "0px";
    
}
